
public class Ifelse {
	public static void main(String[] args) {
		
		int a=56;
		
		if(a<=50) {
			System.out.println("Grade: C");
		}
		else if(a>=50 && a<=60) {
			System.out.println("Grade : B");
		}
		else if(a>=61 && a<=70) {
			System.out.println("Grade : A");
		}
		else {
			System.out.println("Fail");
		}
	}

}
